// Flatten all characters into a single array for easy lookup
export const ALL_CHARACTERS = [];
for (const lineageKey of Object.keys(CHARACTER_MUTATIONS)) {
    for (let i = 0; i < CHARACTER_MUTATIONS[lineageKey].length; i++) {
        ALL_CHARACTERS.push({
            ...CHARACTER_MUTATIONS[lineageKey][i],
            lineage: lineageKey,
            eraIndex: i,
        });
    }
}

// Helper function to get character by lineage and era
export function getCharacter(lineageId, eraIndex) {
    return CHARACTER_MUTATIONS[lineageId]?.[eraIndex] || null;
}

// Helper function to get all characters for an era
export function getCharactersByEra(eraIndex) {
    return Object.keys(CHARACTER_MUTATIONS).map((lineageId) => 
        CHARACTER_MUTATIONS[lineageId][eraIndex]
    );
}

// Helper function to get all characters for a lineage
export function getCharactersByLineage(lineageId) {
    return CHARACTER_MUTATIONS[lineageId] || [];
}

